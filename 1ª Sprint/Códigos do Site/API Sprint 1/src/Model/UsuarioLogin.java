package Model;

//campos de variáveis e métodos de usuario para LOGIN (username, senha)
public class UsuarioLogin {
    String username;
    String senha;
    
    public UsuarioLogin(String username, String senha){
        this.username = username;
        this.senha = senha;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
}
