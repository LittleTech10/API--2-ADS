    private void salvarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_salvarButtonMouseClicked
        String sql = "UPDATE configuracao_canais_usuario SET nome_canal_usuario=?, id_conta_usuario=?, usuario_canal=?, senha_canal=? WHERE id_canal_usuario=?;";
        try {
            stmt = con.prepareStatement(sql);
            stmt.setString(1, editCanalUser.getText());
            stmt.setString(2, editIDContaUser.getText());
            stmt.setString(3, editUser.getText());
            stmt.setString(4, editPassUser.getText());
            stmt.setString(5, editRegistroUser.getText());
            
            stmt.executeUpdate();
            stmt.close();
            con.close();
            
            
        } catch (SQLException e) {
            System.out.println(e);
        }
        this.dispose();
    }//GEN-LAST:event_salvarButtonMouseClicked

